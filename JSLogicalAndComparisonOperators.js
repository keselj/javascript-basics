// Comparison Operators

x = 11;

// equal to (==) 

x == 7; // false
x == 11 // true
x == "11" // true

// equal value and equal type (===)

x === 11; // true
x === "11" // false

// not equal (!=)

x != 7; // true

// not equal value or not equal type (!==)

x !== 11; // false
x !== "11" // true
x !== 7 // true

// greater than (>)

x > 7 // true

// less than (<)

x < 7 // false

// greater than or equal to (>=)

x >= 7 // true

// less than or equal to (<=)

x <= 7 // false


// Logical Operators

x = 11;
y = 7;

// And Operator (&&)

(x < 15 && y > 1) //true

// Or Operator (||)

(x == 7 || y == 12) // false

// not Operator (!)

!(x == y) // true
